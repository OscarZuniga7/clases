import math

# Calcular la raíz cuadrada de 16
resultado = math.sqrt(16)
print(f"La raíz cuadrada de 16 es: {resultado}")

# Calcular el seno de 90 grados (convertido a radianes)
angulo_en_grados = 90
angulo_en_radianes = math.radians(angulo_en_grados)
seno = math.sin(angulo_en_radianes)
print(f"El seno de 90 grados es: {seno}")
